/****商品表****/
  CREATE TABLE product(
    pid INT PRIMARY KEY AUTO_INCREMENT,
    title VARCHAR(64),
    subtitle VARCHAR(64),
    pic VARCHAR(128),
    price DECIMAL(10,1),
    original_price DECIMAL(10,1),
    cid TINYINT
  );
  -- 商品分类表
 CREATE TABLE classify(
    cid INT PRIMARY KEY AUTO_INCREMENT,
    cname VARCHAR(64) 
  );
  -- 用户表
CREATE TABLE user(
  uid INT PRIMARY KEY AUTO_INCREMENT,
  uname VARCHAR(16),
  phone CHAR(11),
  sex TINYINT,
  upwd VARCHAR(32),
  want VARCHAR(128)
);
/****购物车表****/
  CREATE TABLE shoppingCart(
    sid INT PRIMARY KEY AUTO_INCREMENT,
    uid INT,
    pid INT,
    title VARCHAR(64),
    pic VARCHAR(128),
    price DECIMAL(10,1),
    original_price DECIMAL(10,1)
  );

